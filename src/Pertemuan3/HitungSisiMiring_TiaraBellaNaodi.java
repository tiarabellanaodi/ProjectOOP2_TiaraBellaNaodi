public class HitungSisiMiring_TiaraBellaNaodi {
    public static void main(String[] args) {
        double a, b, sisiMiring; // Sisi segitiga siku-siku
        a = 6;
        b = 8;
        sisiMiring = Math.sqrt(a * a + b * b);

        System.out.println("Segitiga siku-siku dengan panjang a = 6 dan b = 8 memiliki sisi miring " );
        System.out.println(sisiMiring);
    }
}
